# a tuple is similar to a list in that it's a place to store multiple values

#  common example is co-ordinates

# tuples are immutable = cannot be changed - constants in effect
# can't change elements, can't add or remove elements etc

# lists use square brackets, tuples use rounded brackets

coordinates = (4,5)

print(coordinates[1])

# it is possible to create a list of tuples - which again are immutable i.e.:

coordinates = [(57,20),(26,54,1,14),(5,4,78,99),(1,2)]
print( coordinates)

